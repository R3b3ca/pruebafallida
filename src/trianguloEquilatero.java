public class trianguloEquilatero {

  //  atributos
    private double area;
    private double perimetro;

    // constructor
    public trianguloEquilatero(double area) {
        this.area = area;
        this.perimetro = perimetro;
    }

    // funciones
    private double getArea() {

        return area;
    }

    private double getPerimetro() {

        return perimetro;
    }
}